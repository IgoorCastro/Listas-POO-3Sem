package exercicio02;

public class Planta{
	
	private String nome;
	private String especie;
	
	public Planta() {
		nome = "Desconhecido";
		especie = "Desconhecida";
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getEspecie() {
		return especie;
	}

	public void setEspecie(String especie) {
		this.especie = especie;
	}
	
	
}
